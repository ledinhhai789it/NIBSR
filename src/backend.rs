//! Real NIBS backend for the NIBSR prototype.
//!
//! This module replaces the earlier development-only Ed25519 transcript signer
//! with the SPS-EQ construction of NIBS from Hanzlik's paper:
//!
//!   KeyGen:  create SPS-EQ key (x1, x2), pk = (g2^x1, g2^x2)
//!   RKeyGen: choose sk_R = x, pk_R = g1^x
//!   Issue:   sign the representative (pk_R, H(nonce || authorization))
//!   Obtain:  adapt the signature by µ = sk_R^{-1}; output
//!            m = H(nonce || authorization)^{sk_R^{-1}}
//!   Verify:  verify the adapted SPS-EQ signature on (g1, m)
//!
//! For the revocable variant, the credential also carries a Chaum-Pedersen
//! DLEQ proof binding (pk_R, nonce, authorization) to m, so a verifier cannot
//! swap pk_R/nonce to bypass NIBSR.RevList.

